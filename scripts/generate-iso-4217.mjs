#!/usr/bin/env node

/**
 * Generates `packages/internal-schemas/src/iso-4217.ts` from the official
 * ISO 4217 currency list published by SIX Group (the ISO 4217 Maintenance
 * Agency).
 *
 * Source: https://www.six-group.com/dam/download/financial-information/data-center/iso-currrency/lists/list-one.xml
 *
 * Run: `node scripts/generate-iso-4217.mjs`
 */

import { writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

import { XMLParser } from "fast-xml-parser";

const ISO_4217_URL =
  "https://www.six-group.com/dam/download/financial-information/data-center/iso-currrency/lists/list-one.xml";

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUTPUT_PATH = resolve(__dirname, "../packages/internal-schemas/src/iso-4217.ts");

async function fetchXml() {
  const res = await fetch(ISO_4217_URL);
  if (!res.ok) throw new Error(`Failed to fetch ISO 4217 list: ${res.status} ${res.statusText}`);
  return res.text();
}

function parseCurrencies(xml) {
  const parser = new XMLParser({
    ignoreAttributes: false,
    attributeNamePrefix: "@_",
    isArray: (name) => name === "CcyNtry",
  });
  const doc = parser.parse(xml);
  const published = doc.ISO_4217?.["@_Pblshd"];
  const entries = doc.ISO_4217?.CcyTbl?.CcyNtry ?? [];

  // Deduplicate by code — multiple countries share the same currency.
  const byCode = new Map();
  for (const entry of entries) {
    const code = entry.Ccy;
    if (!code) continue;
    const name = typeof entry.CcyNm === "object" ? entry.CcyNm["#text"] : entry.CcyNm;
    const isFund = typeof entry.CcyNm === "object" && entry.CcyNm["@_IsFund"] === "true";
    const minorUnits = entry.CcyMnrUnts === "N.A." ? null : Number(entry.CcyMnrUnts);
    if (!byCode.has(code)) {
      byCode.set(code, { code, name, isFund, minorUnits });
    }
  }

  return {
    published,
    currencies: [...byCode.values()].sort((a, b) => a.code.localeCompare(b.code)),
  };
}

function render({ published, currencies }) {
  const enumBody = currencies
    .map(({ code, name, isFund }) => {
      const label = isFund ? `${name} (fund)` : name;
      return `  /** ${label} */\n  ${code}: "${code}",`;
    })
    .join("\n");

  return `// AUTOGENERATED from ISO 4217 list-one — do not edit by hand.
// Source: ${ISO_4217_URL}
// Published: ${published}
// Regenerate with: \`node scripts/generate-iso-4217.mjs\`

/**
 * ISO 4217 currency codes (and fund/precious-metal codes).
 *
 * Sourced from the official list-one.xml maintained by SIX Group on behalf of
 * the ISO 4217 Maintenance Agency. Includes ${currencies.length} entries.
 *
 * Note: maib gateways only accept a subset of these (typically \`MDL\`, sometimes
 * \`EUR\`/\`USD\`). Per-endpoint allowed values are enforced via stricter schemas.
 */
export const Currency = {
${enumBody}
} as const;

export type Currency = (typeof Currency)[keyof typeof Currency];
`;
}

const xml = await fetchXml();
const parsed = parseCurrencies(xml);
const output = render(parsed);
writeFileSync(OUTPUT_PATH, output);
console.log(
  `Wrote ${parsed.currencies.length} currency codes to ${OUTPUT_PATH} (published ${parsed.published})`,
);
